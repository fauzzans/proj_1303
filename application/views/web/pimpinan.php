      <p>Seperti halnya Al Maghfurloh, saudara-saudara beliau pun bergarak dalam dunia Da’wah dan mereka pun merupakan tokoh masyaraka. Adapun saudara-saudara beliau adalah; Kakak; 1). Alm. Hj Suaibah, 2). Alm. KH.Kholil, 3). Alm. KH. Adra’I, 4). Alm. Hj. Zuhro, Adik Alm. KH. Muhammad Basri. Sebagaimana nini mamaknya yang lain, ayahanda beliau pun ( Al_Maghfurloh KH. Abdurrahman )disamping mengelola pesantren ( yang merupakan peninggalan nini mamaknya dan dikelola secara turun temurun sampai dengan sekarang ) dalam menghidupi keluarganya adalah dengan bertani dan berternak ikan. Hal ini merupakan refleksi dari keinginannya untuk memiliki putra putri yang sholih dan sholihah serta keyakinannya bahwa pekerjaan yang paling halal adalah bertani. Keyakinan tersebut terbukti bahwa keluarga beliau (Al-Maghfurloh KH Aburrahman ) terutama yang laki-laki telah menjadi ulama yang menjadi teladan masyarakat.2. Riwayat Pendidikan dan PerjuanaganA. Riwayat Pendidikan
        <br>Al_Maghfurloh hanya mengenyam pendidikan formal sampai kelas 2 (Dua ) sekolah Rakyat, selebihnya beliau dididik oleh ayahandanya. Baru kemudian setelah usianya 11 tahun beliau di kirim ke pesantren Nurul Falah di petir serang, dan selanjutnya meneruskan ke pesantren-pesantren lain di pulau jawa.
        <br>Sebagai seorang yang haus akan ilmu pengetahuan, beliau senantiasa belajar kepada orang-orang yang ahli pada bidangnya. Hal ini tidak hanya berlangsung ketika usianya masih muda namun sampai akhir hayatnya pun senantiasa belajar atas masalah yang kurang atau yang belum di kuasainya. Kecintan beliau akan ilmu menjadikan beliau sebagai orang yang tawadhu, sehingga beliau tidak segan belajar kepada orang yang jauh lebih muda sekalipun darinya.Adapun pesantren-pesantren tempat beliau belajar dan mukim minimal satu (1) tahun antara lain : 1). Pesantren Nurul Falah; 2).Pesantren Buntet, di Cirebon; 3). Pesantren Ternas, di Jawa Timur; 4). Pesantren Pasuruan, Jawa Timur; 5). Pesantren Gentur, di Cianjur; 6). Pesantren Gunung Kawang, Tasikmalaya; 7). Pesantren Kudang, Tasikmalaya; 8) Pesantren Cibarusah. Bekasi; 9). Pesantren Gunung Puyuh, Sukabumi; 10). Pesantren Garisul, Jasinga Bogor; 11). Pesantren Kabagusan, Tenjo Bogor. Sedangkan pesantren tempat beliau belajar dibawah satu tahun tidak terhitung jumlahnya, sebab beliau mempunyai kebiasaan jika menemukan masalah atau cabang suatu ilmu yang belum di fahaminya, beliau akan berguru kepada yang dianggapnya ahli dan tinggal selama beberapa hari atau beberapa bulan hingga beliau faham dan menguasainya.
        <br>Kecintaannya akan ilmu tergambar dari sikapnya yang sangat menghormati orang-orang alim. Hal ini terlihat dari perlakuanya yang ta’zim terhadap mereka. Sebagai ungkapan rasa cinta dan ta’zim, terhadap Guru, beliau juga memperlakukan keluarga gurunya sama seperti kepada sang Guru. Sehingga tidak sungkan mencium tangan putra (laki-laki ) gurunya saat bersalaman.
      </p>
      <p>
      <h5>2. Riwayat Perjuangan</h5>
      Perjuangan yang dilakukan oleh Al-Maghfurloh dapat dibagi kedalam 3 (tiga) priode, yaitu : 1. Masa Revolusi, 2. Setelah Merdeka, 3. Orde Baru.</p>
      <ol>
        <li>Masa Revolusi</li>
        <p>Seperti halnya para pemuda pad masa itu, Al-Maghfuloh pun turut andil bagian dalam merebut dan mempertahankan kemerdekaan Republik Indonesia, dimana pada saat itu beliau sedang menuntut ilmu dipesantren. Dikarnakan panggilan perjuangan merebut kemerdekaan memanggilnya, beliau segera bergabung dalam Laskar Hizbullah, dan ketika berusia 21 tahun beliau menjadi salah satu komandan regu di daerah Bogor barat. Aktivitasnya dalam perjuangan bersenjata menjadikan beliau sebagai orang yang dicari-carioleh Belanda, sehingga kitab-kitab pelajarannya menjadi sasaran mereka dan habis dihancurkan oleh mereka.
          <br>
          Disamping perjuangan dengan mengangkat senjata dalam melawan penjajah, beliau senantiasa mengadakan pertemuan-pertemuan dalam bentuk pengajian dengan sesama pejuang dan masyarakat. Hal tersebut dilakukan untuk mengelabui musuh. Adapun dari pengajian yang dilaksanakan adalah berupa :
        </p>
        <ul>
          <li>Pengembangan mental para pejuang dan masyarakat</li>
