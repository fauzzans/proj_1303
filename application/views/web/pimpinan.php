        <li>Setelah Merdeka</li>
        <p>Pada masa pasca kemerdekaan ini, Al-Maghffurlah aktif membina masyarakat, baik melalui pengajian-pengajian maupun melalui organisasi, yaitu masyumi dan kemudian bergabung dengan Nahdltul Ulama.
          <br>
          Selain itu, pada tahun 1946 s/d 1948 akhir, beliau pernah memegang tampuk pemerintahan yaitu menjadi Camat Revolusi didaerah Lebak Wangi, yang pada waktu itu meliputi wilayah Jasinga, Parung Panjang, Cigudeg dan Tenjo dan berpusat di Desa Rengas Jajar.
          <br>
          Ketika terjadi genjatan senjata dengan pihak belanda dan seluruh pasukan dilebur kedalam TNI serta dikirim ke Sumbawa dan daerah lainya, beliau memilih mengundurkan diri dari pasukan perang dan lebih mengkonsentrasikan diri dalam pembinaan mental spiritual masyarakat. Untuk lebih mengefektifkan pengabdianya dalam dunia Da’wah dan pendidikan umat, serta untuk mendidik dirinya sendiri agar mampu mandiri, maka pada tahun 1952 Beliau merintis pendirian Pesantren “Nurul Falah” yang dilengkapi dengan Madrasah Ibtidaiyah di desa Sindang Pala kecamatan Semplak.
          <br>
          Adapun pada masa Orde Lama, beliau aktif dalam Organisasi Politik, yaitu menjadi anggota Masyumi yang kemudian mengundurkan diri dan memilih bergabung dengan Nahdlatul Ulama. Aktifitas dalam organisasi tersebut telah mewarnai pola fikir beliau. Seperti yang telah diketahui, bahwa beliau adalah orang yang sangat terbuka terhadap perbedaan pendapat. Beliau pernah mengatakan bahwa “ kemampuanya dalam bertukar fikiran (mengadu argumentasi dalam mencari), kelapangan dada dalam perbedaan pendapat, diperoleh dari didikan organisasi-organisasi.
          <li style="text-align: justify;">Sebelum mengundurkan diri dari kegiatan organisasi masyarakat dan politik, Almaghfurlah KH. Istichori bersama-sama dengan KH. Zabidi (Allahuyarham) membidani kelahiran Nahdlatul Ulama Kabupaten Bogor yang pada waktu itu masih satu kesatuan pengurus dengan Kota Bogor. Disamping itu beliau merupakan salah seorang ulama yang turut serta membidani kelahiran Majlis Ulama Jawa Barat, yang kelak menjadi cikal bakal MUI.
            <br>Setelah mengundurkan diri dari kegiatan politik praktis, beliau lebih mengkonsentrasikan diri dan mengabdikan seluruh kehidupanya bagi kemajuan pendidikan umat Islam. Dimana selain mengasuh dan membina pondoknya sendiri, pada tahun 1967 sampai beberapa tahun lamanya beliau menjadi dosen di IAIN Syarif Hidayatullah Bogor, juga mengisi pengajian-pengajian rutin atau sebagai guru di pesantren, Majlis-majlis Ta’lim, masjid dan tempat lainya diberbagai daerah yang dilakukan hingga akhir hayatnya.
            <br>Kegiatan lain yang sering beliau lakukan adalah senantiasa belajar dan mempelajari ilmu agama dengan langsung mendatangi orang-orang yang dianggapnya ‘Alim. Beliau juga sering mengikuti seminar dan lokakarya baik sebagai nara sumber maupun peserta, hal ini dilakukanya dalam rangka Tafaqquh fiddin dan Da’wah Islamiyah.
            <br>Pengalaman Almaghfurlah dalam dunia organisasi dan politik praktis menjadi bekal baginya dalam membina ummat. Beliau senantiasa berusaha berada ditengah-tengah semua golongan, yaitu dengan tujuan agar ummat tidak terpecah belah oleh firqoh politik dan perbedaan mazhab. Sikap ini diimplementasikan dalam kebijakanya dalam mengelola Pondo Pesantren Darut Tafsir, sehingga Pondok Pesantren Darut Tafsir merupakan lembaga yang tidak berafliasi dengan Organisasi keagamaan dan politik manapun, atau “ La syarqiyyah wa la Ghorbiyyah”.
            <br>Setelah upaya merintis pendidikan Pesantren Nurul falah di Sindangpala Kecamatan Semplak berjalan beberapa waktu, maka pada akhir tahun 1971 dengan modal hasil penjualan sawah dan rumah peninggalan ayahanda istri beliau (Hj. Rasmani) beliau merintis kembali pendirian pesantren yang diberi nama “Darut Tafsir” di desa Gunung Batu kecamatan Ciomas, dan sekitar tiga tahun kemudian, yaitu pada tanggal 5 Mei 1974 beliau memboyong seluruh keluarga dan santrinya ke desa Cibanteng Kecamatan Ciampea. Hal ini dilakukan untuk lebih mengembangkan pesantren, karena di tempat yang lama tidak memungkinkan untuk pengembangan lebih lanjut.
