        Adapun silsilah keluarga beliau adalah; Garis silsilah dari pihak ayah ; KH. Muhammad Istichori bin KH. Abdurrahman bin KH. Manarah bin KH. Beran bin KH. Nurjen bin mas dalem Anggayudha bin. Pangeran Aryawinata ( Pangeran Sumedang ) dan seterusnya.Sedangkan silsilah dari pihak Ibu adalah ; Hj. Aemi binti KH. Asyirun bin KH. Beran dan seterusnya. Dari silsilah tersebut dari pihak ayah maupun dari pihak ibu masih ada ikatan keluarga dekat, yaitu bersumber dari keturunan KH. Baran bin Mas Dalem Angga Yudha bin Pangeran Aryawinata ( Pangeran Sumedang ).
