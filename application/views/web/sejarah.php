      <p>Lahirnya Perguruan-perguruan Tinggi adalah natijah dari pada usaha ini. Banyak pesantren yang masih belum melihat kenyataan. Mereka masih mengikuti cara lama. Usaha modernisasi sekalipun lambat, tapi masih berjalan terus. Kesulitan-kesulitanya datang dari pesantren itu sendiri. Kalau dilihat dari sejarahnya, logis. Sebab pada umumnya pesantren-pesantren di Jawa Barat hanya mengutamakan pelajaran-pelajaran Ilmu Fiqih saja, tapi jarang yang sampai mempelajari ilmu perbandinganya.

        Ilmu-ilmu yang penting seperti: Sejarah Umum, sejarah Islam, tidaklah masuk dalam pelajaran pesantren, padahal ilmu itu sangat dianjurkan oleh Alqur’an. Sedang kan pelajaran Tafsir Alqur’an kebanyakan hanya terbatas kepada ayat ahkam saja. Itupun masih sangat sederhana. Sedangkan ayat yang mengenai ilmu Pengetahuan Umum tidak disentuhnya. Kesemuanya itu mengakibatkan:
      <ol type="a">
