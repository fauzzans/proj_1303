        </h4>
        <p>Setelah menyelesaikan pelajaran disekolah dan pesantren diberbagai tempat, di Jawa Barat, Jawa Tengah dan Jawa Timur bersamaan dengan berakhirnya revolusi fisik, tibalah waktunya untuk mengisi kemerdekaan. Maka pada tahun 1952, saya mendirikan pesantren yang dilengkapi dengan sekolah Ibtidaiyah, bertempat di Sindangpala Semplak Bogor.
          Kekurangan-kekurangan setelah kemerdekaan sangat terasa. Pesantren yang telah ada tidak memenuhi kebutuhan suatu bangsa yang sedang membangun. Keadaan ini menimbulkan berbagai upaya dari tokoh-tokoh Pesantren.
