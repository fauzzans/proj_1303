        <p><strong>KABUPATEN</strong> <strong>BOGOR</strong> – Pemerintah Kecamatan Ciampea, Kabupaten Bogor kembali melakukan penyemprotan disinfektan di 4 titik pondok pesantren dan Mesjid Raya, dua wilayah Desa Bojong Rangkas dan Cibanteng yang dipimpin langsung Camat Ciampea serta dampingi Kepala Desa di dua lokasi, Desa Bojong Rangkas dan Cibanteng, Kecamatan Ciampea, kabupaten Bogor. (1/4).</p>
        <p>“Hari ini kita langsung menyemprot kembali disinfektan ke beberapa titik yaitu mesjid raya Al’ hidayah, ponpes Nurut Taqwa putra/putri, yang ada di wilayah desa Bojong Rangkas serta ponpes Darut Tafsir yang ada di wilayah desa Cibanteng dan satu lagi mesjid raya Jami Al’ Hidayah. ” Kata Camat Ciampea, Chaerudin Felani.</p>
        <p>Penyemprotan disinfektan ini di lakukan untuk memutus rantai persebaran Covid-19 dan dibantu oleh Satpol PP Kecamatan Ciampea secara bergantian.</p>
        <p>Pimpinan Darut Tafsir, Hasan Basri mengapresiasi kegiatan tersebut dengan diadakannya penyemprotan disinfektan di Pondok Pesantrennya.</p>
        <img class="img-fluid rounded" src="img/disinfektan1.jpg" alt="">
        <p>“Alhamdulillah kami telah didatangi Pak Camat dan jajarannya yang telah menyemprotkan disinfektan di Ponpes Darut Tafsir, dengan adanya penyemprotan ini kami sangat berterima kasih sudah diperhatikan.” Katanya.</p>
        <p>Hal senada dilontarkan Kepala Yayasan Pondok Pesantren Nurut Taqwa Ade Mumu Najmuddin, dengan penyemprotan disifektan tersebut memberikan rasa aman pada santri dan pengurus pondok pesantren.</p>
        <p>“Alhamdulalih saya berterima kasih kepada camat Ciampea beserta jajarannya yang sudah perduli membantu pondok pesantren kami dengan melakukan penyemprotan disinfektan, mudah-mudahan santri-santri kami sehat dan tidak terjadi hal yang di inginkan, terkait Corona ini,” ujarnya.</p>
